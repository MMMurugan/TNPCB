<script>
    $(document).ready(function() {
        var table = $('#board-proceedings-table').DataTable();

        // Search functionality for filtering
        $('#search-bar').on('keyup', function() {
            table.search(this.value).draw();
        });

        // Year filter functionality
        $('#year-filter').on('change', function() {
            var selectedYear = this.value;
            table.rows().every(function() {
                var year = $(this.node()).data('date').split('-')[0]; // Extract year from data-date
                $(this.node()).toggle(year === selectedYear || !selectedYear);
            });
            table.draw(); // Redraw the table after filtering
        });
    });
</script>
