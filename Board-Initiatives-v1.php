                <h5 class="sec_title mb-4 text-center">Energy Conservation</h5>
                <p class="lead mt-4" style="text-align: justify;">
                    As a measure for fuel conservation and recovery, all the distilleries are recovering methane gas from their spent wash through anaerobic digestion. Major sugar factories have installed co-generation power plants. The sago units recover methane gas from their trade effluent through anaerobic digestion. Activated carbon manufacturing units have gone for waste heat recovery boiler and eliminated the dedicated boiler to produce steam for the activation purpose. This system eliminated the consumption of coal / wood as fuel for the boiler and thus avoided greenhouse gaseous emission. Other industrial units are encouraged to use less energy and it is being audited through the environmental statements of the unit. TNPCB has installed solar panels for all its 18 own office buildings to get solar energy. The new buildings which are under construction will be provided with solar panels.</p>
                    <h5 class="sec_title mb-4 text-center">Technology Demonstration Centre	</h5>
                    <p class="lead mt-4" style="text-align: justify;">
                        TNPCB on enforcement and its monitoring encourages Cleaner Technology, Waste Minimization Technology, Hazardous substance elimination by alternate process, reuse, and recycle systems, zero liquid discharge and best available technology to promote industrial development within the sustainable development in the State of Tamilnadu. As the number of industries in Tamil Nadu is increasing at a rapid rate, crossing over 46000, the enforcement authorities and industries find it challenging to attain sustainable development and to derive suitable solution to address the environmental issues. In order to find a solution, TNPCB has established a Technology Demonstration Centre at Indian Institute of Technology Madras and have entered into a Memorandum of Understanding (MoU). A sum of Rs.5 crores has been allocated for this purpose. The Technology Demonstration Centre has conducted treatability studies for rice mills, sago and small scale textile processing units. The Centre has conducted awareness programme on fire management on waste landfill site for the Municipal Authorities, Fire Department and TNPCB officials. The Centre has also conducted technical workshop for sago industries for better understanding of the characteristics of sago waste water, anaerobic treatment, biogas generation, optimizing the process parameters to enhance the composition and quantity of biogas generated. This Centre designed and fabricated the Upflow Anaerobic Sludge Blanket Reactor (UASB) to demonstrate best available technology for anaerobic treatment of sago waste water and energy generation. Further work in this regard is continuing.
</p>
