            <h4 id="public-participation" class="m-0 sec_title_env text-center my-3">Public Participation</h4>
            <section>
                <h4 class="m-0 sec_title_env text-center m-3">ECOmmute School - Certificate Programme</h4>
                <p>
                    Air pollution is a major environmental risk to health. Vehicular pollution from use of fossil fuels is a
                    major contributor for air pollution in cities. The emissions also contribute directly to Climate Change
                    phenomenon making wild as well as human dominated ecosystems very vulnerable to disasters.
                    Considering the importance of environment protection from air pollution, the Tamil Nadu Pollution
                    Control Board (TNPCB) have initiated a 'Weekly Pollution-Free Office Commute Day' (ECOmmute Day)
                    and commenced it from 17th November 2021, a Wednesday. Accordingly, every Wednesday, the
                    employees and all staff of TNPCB in all the offices of State avoid personal motorized vehicles and reach
                    the office premises by either public transport or walk or cycle or electric cycles/vehicles. Following the
                    initiative of TNPCB, several District Collectors across the country and the Administration of
                    Lakshwadeep have also implemented the ECOmmute Day. Several industries are also implementing
                    ECOmmute. An exclusive logo has been released to mark the 'ECOmmute'.
                    In order to extend the ECOmmute concept to school students, the TNPCB have launched a programme
                    titled 'ECOmmute School'. The objective of the programme is to encourage high school students to
                    adopt 'ECOmmute' mode to commute to school. This was initiated to encourage students to adopt
                    green modes of transport like walk or use cycle or public transport to attend school and also reduce
                    the local air pollution in the vicinity of schools.
                    As per the programme, the schools wherein 90% and above students adopt ECOmmute mode will be
                    awarded with certificate of 'ECOmmute School' and the students will be presented with 'Friend of
                    Environment' certificate. In addition, the top 10 Government schools and top 10 Govt aided/private
                    school will be awarded with sports articles worth of Rs.25,000/- each.
                    As a first phase, the TNPCB introduced the 'ECOmmute School' programme to the Schools in Chennai
                    District in the year 2021-2022. The students in the 8th, 9th and 10th Standards are covered in this
                    programme. TNPCB has addressed to all the Government, Government Aided and Private Schools in
                    Chennai district to participate in this programme. The schools are informed that ECOmmute is a
                    voluntary proramme. No student is compelled to participate in this programme. They shall participate
                    in the programme with the concurrence of their parents. In this regard, circular instructions were also
                    issued from the Chief Education Officer, Chennai District. The TNPCB has also addressed to the
                    Commissioner, Greater Chennai Corporation to make the roads and foot paths near the school are safe
                    for commute by the students. The TNPCB has also addressed to the Commissioner of Police, Greater
                    Chennai Police for declaration of one hour in morning and evening as 'Student Hour' in road traffic and
                    also to provide barricades near the schools.
                    The TNPCB has developed an online portal (https://tnpcb.gov.in/ecommute_org/) for registration to
                    participate in the programme. This software, itself calculate the percentage of the students adopted
                    ECOmmute mode. [% of ECOmmute = (X/Y) x 100. Where X = Total number of students in 8th, 9th &
                    10th Std. adopted in ECOmmute mode, Y = Total students strength in 8th, 9th & 10th Std.].
                    The Board has provided hands-on training to all the schools on how to registrar through the e.portal.
                    242 schools have attended the training programme. In the above program, 99 schools with student
                    strength of 24627 in Chennai city have registered and participated. Out of which, 37 schools (21 Govt.
                    + 16 Govt. Aided and Private) with student strength of 6373 have achieved 90% and above ECOmmute
                    mode.
                    During 21st & 22nd May 2022, the Ministry of Environment, Climate Change and Forest, Government
                    of India has conducted the two days Workshop on National Clean Air Programme at Chennai. The
                    workshop was inaugurated by the Hon'ble Minister for Environment, Climate Change and Forest,
                    Government of India. In order to publicize the ECOmmute at the National level, best performed six
                    schools (3 Govt + 3 Govt Aided and Private) were invited and presented with'ECOmmute School'
                    certificate in the abovesaid inaugural function. The remaining 31 schools were presented with
                    certificate and the top 20 schools were awarded sports equipments by the Hon'ble Minister for
                    Environment, Climate Change, Youth Welfare and Sports Development during the World Environment
                    Day celebration held on 5th June 2022. The students were presented with 'Friend of Environment'
                    Certificate.
                </p>
            </section>

