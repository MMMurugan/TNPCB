            <section>
                <p>
                    Every year, the Board conducts public awareness programme during Deepavali festival for celebrating
                    noise and smoke pollution free Deepavali. The Hon'ble Supreme Court of India in its Judgment dated
                    23.10.2018 in W.P. (C) No.728/2015 have issued directions inter-alia that the crackers with reduced
                    emission (improved crackers) and green crackers only would be permitted to be manufactured and
                    sold, extensive public awareness campaigns shall be taken up by the Central Government / State
                    Government / Schools / Colleges informing the public about the harmful effects of firecrackers. The
                    Hon'ble Court has also restricted the timing for two hours for bursting of fireworks. Accordingly, the
                    Government of Tamilnadu has permitted the public for bursting of crackers between 6.00 a.m. to 7.00
                    a.m. and 7.00 p.m. to 8.00 p.m. on the Deepavali day (04.09.2021). The Hon'ble Supreme Court has
                    also directed the SPCBs to carryout short-term monitoring in their cities for 14 days (commencing from
                    7 days prior to Deepavali and ending 7 days after Deepavali).As per the directions of the Hon'ble
                    Supreme Court, the Board conducted awareness programme in association with Eco-clubs, National
                    Green Corps, School Education, Higher Education, Police Departments and District Administration. In
                    this regard, a total fund of Rs.6.5 lakhs (Rupees Six lakhs and Fifty Thousands only) was sanctioned to
                    all the District Environmental Engineers for conducting awareness programme. Awareness through FM
                    Radio was also done for four days from 01.11.2021 to 04.11.2021 in 9 cities (i.e.) Chennai, Trichy,
                    Coimbatore, Madurai,Tirunelveli, Tuticorin, Vellore, Salem and Erode.
